package org.connectorio.addons.binding.relayweblog.client;

import org.apache.commons.codec.digest.DigestUtils;
import org.apache.commons.codec.digest.HmacAlgorithms;
import org.apache.commons.codec.digest.HmacUtils;

public class Hash {

  static String sha512(String value) {
    return DigestUtils.sha512Hex(value);
  }

  static String hmacsha512(String value, String key) {
    return new HmacUtils(HmacAlgorithms.HMAC_SHA_512, key).hmacHex(value);
  }

}
